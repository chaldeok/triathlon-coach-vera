var menuBody = document.querySelector('.main-menu__body');
var button = document.querySelector('.burger-menu__button');
var links = document.querySelectorAll('.main-menu__link');

function burgerMenu() {
  button.addEventListener('click', function (e) {
    e.preventDefault();
    toggleMenu();
  });
  links.forEach(function (link) {
    link.addEventListener('click', function () {
      toggleMenu();
    });
  });
}

function toggleMenu() {
  menuBody.classList.toggle('burger-menu--active');
  button.classList.toggle('burger-menu__button--active');
}

burgerMenu();